    <h1> Getting Started on Windows and Mac <a class="article-subscribe" title="Opens a sign-in dialog" rel="nofollow" role="button" data-auth-action="signin" aria-selected="false" href="/hc/en-us/articles/201362033-Getting-Started-on-Windows-and-Mac/subscription.html">Follow</a></h1>

  </header>

  <div class="subtle-wrap">
    <div class="article-body markdown">
      <h2>Overview</h2>
<p>Zoom unifies cloud video conferencing, simple online meetings, and group messaging into one easy-to-use platform. Our solution offers the best video, audio, and wireless screen-sharing experience across multiple platforms. Follow this article to learn about the basic features of the Zoom client on Windows and Mac.</p>
<h2>Prerequisites</h2>
<ul>
<li><a href="https://zoom.us/support/download" target="_self">Zoom desktop client (Windows or Mac)</a></li>
</ul>
<p>This article covers:</p>
<ul>
<li><a href="#h_97174a74-246a-4e54-8e9c-22b55cd0b665" target="_self">Sign in and Join</a></li>
<li><a href="#h_8968ed2e-21e9-420f-9a16-1e0fdd6fefc2" target="_self">Home</a></li>
<li><a href="#h_14d1c20e-fc7a-4983-849f-6f11d84ccb68" target="_self">Chat</a></li>
<li><a href="#h_074b0ef0-3030-4b28-8dbd-04cb8adc82b6" target="_self">Phone</a></li>
<li><a href="#h_32134caa-8e41-4ed8-9519-98f0129cd25b" target="_self">Meetings</a></li>
<li><a href="#h_f0a32e7a-d84c-431e-abf6-78660aab46db" target="_self">In-Meeting Controls</a></li>
<li><a href="#h_3898f0ef-b27c-4bb1-a54b-9c69579b7241" target="_self">More Resources</a></li>
</ul>
<h2 id="h_97174a74-246a-4e54-8e9c-22b55cd0b665">Sign in and Join</h2>
<p>After launching Zoom, click&nbsp;<strong>Join a Meeting</strong> to <a href="https://support.zoom.us/hc/en-us/articles/201362193-Joining-a-Meeting" target="_self">join a meeting</a> without signing in. If you want to log in and start or schedule your own meeting, click <strong>Sign In</strong>.</p>
<p><img src="https://assets.zoom.us/images/en-us/desktop/generic/home/join-meeting-or-sign-in-screen.png" alt=""></p>
<p>To&nbsp;sign in, use your Zoom, Google, or Facebook account. You can also <a href="https://support.zoom.us/hc/en-us/articles/201800126" target="_self">log in using SSO</a>.&nbsp;If you don't have an account, click <strong><a href="https://zoom.us/signup" target="_blank" rel="noopener noreferrer">Sign Up Free</a></strong>. If you have a Zoom account but cannot&nbsp;remember&nbsp;your password, click <strong>Forgot</strong>. <br><img src="https://assets.zoom.us/images/en-us/desktop/generic/home/sign-in-screen.png" alt=""></p>
<h2 id="h_8968ed2e-21e9-420f-9a16-1e0fdd6fefc2">Home</h2>
<p>After signing in, you will see the <strong>Home</strong> tab, where you can click these options:</p>
<p><img src="https://assets.zoom.us/images/en-us/desktop/generic/home/home-screen.png" alt=""></p>
<ul>
<li><strong>New Meeting</strong>: Start an <a href="https://support.zoom.us/hc/en-us/articles/201362533-Meet-Now-vs-Scheduled-Meetings" target="_self">instant meeting</a>. Click the downwards arrow to enable video or use your <a href="https://support.zoom.us/hc/en-us/articles/203276937-Using-Personal-Meeting-ID-PMI-" target="_self">personal meeting ID (PMI)</a> for instant meetings.</li>
<li><strong>Join</strong>:&nbsp;<a href="https://support.zoom.us/hc/en-us/articles/201362193-Joining-a-Meeting" target="_self">Join a meeting</a> that is in progress.</li>
<li><strong>Schedule</strong>: <a href="https://support.zoom.us/hc/en-us/articles/201362413-How-Do-I-Schedule-Meetings-" target="_self">Set up a future meeting</a>.</li>
<li><strong>Share Screen</strong>: <a href="https://support.zoom.us/hc/en-us/articles/203727929-Screen-Sharing-with-Zoom-Rooms" target="_self">Share your screen in a Zoom Room</a> by entering in the sharing key or meeting ID.</li>
<li>Date and time with background image: To change the background image, hover over the picture and click the camera icon.<br> <img src="https://assets.zoom.us/images/en-us/desktop/generic/home/date-and-time-with-background-image.png" alt=""></li>
<li>Upcoming meeting: Displays the next meeting for the current day. <a href="/hc/en-us/articles/115005506303" target="_self">Add a third-party calendar service</a> if you want to sync upcoming meetings.<br> <img src="https://assets.zoom.us/images/en-us/desktop/generic/home/upcoming-meeting.png" alt=""></li>
<li>Click your profile picture for these options:<br> <img src="https://assets.zoom.us/images/en-us/desktop/generic/home/status-menu-add-personal-note.png" alt=""><br>
<ul>
<li><a href="https://support.zoom.us/hc/en-us/articles/115002571866-Personal-Note-for-Your-Chat-Profile" target="_self">Add a personal note</a><strong>.</strong></li>
<li><strong>Settings</strong>: Access <a href="/hc/en-us/articles/201362623" target="_self">settings</a> you can change in the client.</li>
<li>Change your status to <strong>Available</strong>, <strong>Away</strong>, or <strong>Do Not Disturb</strong>.</li>
<li><strong>My Profile</strong>: Open the Zoom web portal to <a href="https://support.zoom.us/hc/en-us/articles/201363203-My-Profile" target="_self">edit your profile</a>.</li>
<li><strong>Help</strong>: Open the <a href="https://support.zoom.us/hc" target="_self">Zoom Help Center</a>.</li>
<li><strong>Check for Updates</strong>: Check if Zoom is up to date.</li>
<li><strong>About Zoom</strong>: View the current version.</li>
<li><strong>Switch to Portrait View</strong>: Switch the Zoom window to portrait view if you prefer a narrower window.</li>
<li><strong>Sign out</strong></li>
<li><strong>Upgrade to Pro</strong> (if you are on a free account)</li>
</ul>
</li>
</ul>
<h2 id="h_14d1c20e-fc7a-4983-849f-6f11d84ccb68">Chat</h2>
<p>Select the <strong>Chat</strong> tab to view private conversations with your contacts or group conversations (<a href="/hc/en-us/articles/200912909" target="_self">channels</a>).</p>
<p><img src="https://assets.zoom.us/images/en-us/desktop/generic/chat/chat-tab.png" alt=""></p>
<p>You can access these features in the left-side panel:</p>
<p><img src="https://assets.zoom.us/images/en-us/desktop/generic/chat/left-side-panel.png" alt=""></p>
<ul>
<li><strong>Jump to</strong> search box: Search for a contact or channel.</li>
<li><strong>Starred Messages</strong>: View messages you have <a href="https://support.zoom.us/hc/en-us/articles/115004789183-Starring-Editing-and-Deleting-Chat-Messages" target="_self">starred</a>.</li>
<li>Click your name to use your <a href="https://support.zoom.us/hc/en-us/articles/360021806951-Using-your-Personal-Chat-Space" target="_self">personal chat space</a>.</li>
<li>Add icon <img src="https://assets.zoom.us/images/en-us/desktop/generic/chat/add-icon.png" alt="">&nbsp;(next to <strong>Recent</strong>): Start a new chat with one of your contacts, <a href="https://support.zoom.us/hc/en-us/articles/200912909-Getting-Started-With-Channels-Group-Messaging-" target="_self">create a channel</a> for group chats, or <a href="https://support.zoom.us/hc/en-us/articles/200912909-Getting-Started-With-Channels-Group-Messaging-" target="_self">join an existing channel</a>.</li>
<li>Under <strong>Recent</strong>, click a contact to view chat history and continue to chat with them. Contacts have a <a href="https://support.zoom.us/hc/en-us/articles/360032554051" target="_self">status icon</a> before their name.<br> <img src="https://assets.zoom.us/images/en-us/desktop/generic/chat/left-side-panel-contact-name.png" alt=""></li>
<li>Under <strong>Recent</strong>, click a <a href="https://support.zoom.us/hc/en-us/articles/200912909-Getting-Started-With-Channels-Group-Messaging-" target="_self">channel</a> (indicated by the group icon before the name) to view the channel's chat history and send messages to channel members.<br> <img src="https://assets.zoom.us/images/en-us/desktop/generic/chat/left-side-panel-channel-name.png" alt=""></li>
</ul>
<p>After selecting a contact or channel in the left-side panel, you can use these features in the chat window:<br> <img src="https://assets.zoom.us/images/en-us/desktop/generic/chat/chat-history-and-compose-chat.png" alt=""></p>
<ul>
<li>Star icon <img src="https://assets.zoom.us/images/en-us/desktop/generic/chat/star-icon.png" alt="">: Add the contact or channel to your <a href="https://support.zoom.us/hc/en-us/articles/115004055706-Managing-Contacts" target="_self">starred list</a>, giving you quick access to those contacts or channels.</li>
<li>Video icon&nbsp;<img src="https://assets.zoom.us/images/en-us/desktop/generic/chat/video-icon.png" alt="">: Start a meeting with the contact. If a channel is selected, this will start a meeting with all members of the channel.</li>
<li>New window icon&nbsp;<img src="https://assets.zoom.us/images/en-us/desktop/generic/chat/new-window-icon.png" alt="">&nbsp;(hover your pointer over the contact or channel name to display this icon) : Opens the selected chat in a new window.</li>
<li>Info icon&nbsp;<img src="https://assets.zoom.us/images/en-us/desktop/generic/chat/info-icon.png" alt="">: View additional options for the contact or channel. Also gives you quick access to files, images, and <a href="https://support.zoom.us/hc/en-us/articles/115004789183-Starring-Editing-and-Deleting-Chat-Messages" target="_self">starred messages</a> in the selected chat.</li>
<li>Message box: Compose and send messages to your contact or channel. You can also send <a href="https://support.zoom.us/hc/en-us/articles/202920879-Capture-Screen-Send-Picture-File-Transfer" target="_self">screenshots</a>, <a href="https://support.zoom.us/hc/en-us/articles/202920879-Capture-Screen-Send-Picture-File-Transfer" target="_self">files</a>,&nbsp;<a href="/hc/en-us/articles/360000489103" target="_self">code snippets</a>, and <a href="/hc/en-us/articles/115004802246" target="_self">animated GIFs</a>.</li>
</ul>
<h2 id="h_074b0ef0-3030-4b28-8dbd-04cb8adc82b6">Phone</h2>
<p>Select the <strong>Phone</strong> tab to make phone calls, view call history, and play voicemail messages using <a href="/hc/en-us/articles/360028936531" target="_self">Zoom Phone</a>.</p>
<p><strong>Note</strong>: Zoom Phone licence is required.</p>
<p><img src="https://assets.zoom.us/images/en-us/desktop/generic/phone/phone-tab.png" alt=""></p>
<p>&nbsp;</p>
<ul>
<li><strong>History</strong> tab: View and delete <a href="/hc/en-us/articles/360021336671" target="_self">call history and recordings</a>.</li>
<li><strong>Voicemail</strong> tab: Play and manage <a href="/hc/en-us/articles/360021400211" target="_self">voicemail messages</a>.</li>
<li>Dial pad: <a href="/hc/en-us/articles/360021087192" target="_self">Make outbound calls</a> by manually entering a phone number or searching through your contacts.</li>
</ul>
<p>Learn more about using <a href="/hc/en-us/articles/360028936531" target="_self">Zoom Phone features</a>.</p>
<h2 id="h_32134caa-8e41-4ed8-9519-98f0129cd25b">Meetings</h2>
<p>Select the <strong>Meetings</strong> tab and click <strong>Upcoming</strong> to view, start, edit, and delete scheduled meetings.</p>
<p><img src="https://assets.zoom.us/images/en-us/desktop/generic/home/meetings-screen.png" alt=""></p>
<p>You can click the following options after selecting a scheduled meeting in the left-side panel:</p>
<ul>
<li>Add icon&nbsp;<img src="https://assets.zoom.us/images/en-us/desktop/generic/meetings/add-icon.png" alt="">: <a href="https://support.zoom.us/hc/en-us/articles/201362413-How-Do-I-Schedule-Meetings-" target="_self">Schedule a new meeting</a>.</li>
<li>Refresh icon&nbsp;<img src="https://assets.zoom.us/images/en-us/desktop/generic/meetings/refresh-icon.png" alt="">: Refresh the meeting list if you don't see your scheduled meeting.</li>
<li><strong>Start</strong>: <a href="https://support.zoom.us/hc/en-us/articles/201362423-How-do-I-Start-or-Join-a-Scheduled-Meeting-as-the-Host-" target="_self">Start the scheduled meeting</a> that you have selected in your upcoming meetings.</li>
<li><strong>Copy Invitation</strong>: Copy the scheduled meeting's <a href="https://support.zoom.us/hc/en-us/articles/201362773-Where-can-I-find-the-meeting-invitation-text-" target="_self">invitation text</a>, allowing you to manually paste into an email, instant messenger etc. Click <strong>Show Meeting Invitation</strong> if you want to view the invitation you're copying.</li>
<li><strong>Edit</strong>: Edit the <a href="https://support.zoom.us/hc/en-us/articles/201362413-How-Do-I-Schedule-Meetings-" target="_self">meeting options</a> for&nbsp;your scheduled meeting.</li>
<li><strong>Delete</strong>: Permanently delete your scheduled meeting.</li>
</ul>
<h2>Contacts</h2>
<p>Select the <strong>Contacts</strong> tab to view and manage your contacts.</p>
<p><img src="https://assets.zoom.us/images/en-us/desktop/generic/contacts/contacts-tab.png" alt=""></p>
<ul>
<li><strong>Directory</strong> tab: View a directory of all your contacts, including <a href="https://support.zoom.us/hc/en-us/articles/115004055706" target="_self">starred</a>, external, and <a href="https://support.zoom.us/hc/en-us/articles/203736135" target="_self">auto answer</a> contacts. If your organization has Zoom Rooms, you will also see a list of Zoom Rooms.</li>
<li><strong>Channels</strong> tab: View a directory of your channels (used for group messaging). Starred channels appear at the top of the list.</li>
<li>Add icon <img src="https://assets.zoom.us/images/en-us/desktop/generic/meetings/add-icon.png" alt="">: Show options for <a href="https://support.zoom.us/hc/en-us/articles/115004055706" target="_self">contacts</a> and <a href="https://support.zoom.us/hc/en-us/articles/200912909" target="_self">channels</a>. You can add a contact, create a contact group, create a channel, or join a channel.</li>
</ul>
<h2 id="h_f0a32e7a-d84c-431e-abf6-78660aab46db">In-Meeting Controls</h2>
<p>Once you have started or joined a meeting, you can access the meeting controls located at the bottom of the meeting window (move your mouse in the Zoom window to display meeting controls).</p>
<p>Learn more about meeting controls for <a style="background-color: #ffffff;" href="https://support.zoom.us/hc/en-us/articles/201362603-Host-Controls-in-a-Meeting" target="_self">hosts</a>, <a style="background-color: #ffffff;" href="https://support.zoom.us/hc/en-us/articles/360016056751-Co-host-controls-in-a-meeting" target="_self">co-hosts</a>, and <a style="background-color: #ffffff;" href="https://support.zoom.us/hc/en-us/articles/200941109-Attendee-Controls-in-a-Meeting" target="_self">attendees</a>. You can also <a href="https://support.zoom.us/hc/en-us/articles/115002262083-Joining-a-Test-Meeting" target="_self">join a test meeting</a> to familiarize yourself with meeting controls before joining a scheduled meeting.</p>
<p><img src="https://assets.zoom.us/images/en-us/desktop/generic/in-meeting/meeting-controls-with-marketing-images.png" alt=""></p>
