        <p><em>source: <a href="https://en.wikipedia.org/wiki/Shiur_(Torah)" target="_blank">wikipedia </a></em></p>
        <h2>Overview</h2>
        <p><strong>Shiur</strong> (/ˈʃiːər/, Hebrew: שיעור [ʃiˈʔuʁ], pl. shiurim, שיעורים [ʃiʔuˈʁim] lit. "Lesson") is a lesson on any Torah topic, such as Gemara, Mishnah, halakha, Tanakh, etc.</p>
        <h2>History</h2>
        <p>The shiur has been a primary method of teaching since Mishnaic times. In a famous Talmudic passage, Rabbi Judah haNasi averred that he gained his sharp mind from watching Rabbi Meir deliver the shiur. However, since the lecture hall was so crowded, his seat was behind the lectern, so he only saw Rabbi Meir from the back. "Had I seen him from the front, how much greater would I have become!" he declared.[1]</p>
        <h2>Yeshiva learning</h2>
        <p>Traditionally, a shiur refers to the type of learning that takes place in yeshivot and kollelim, in which students hear an in-depth lecture on the sugya (Gemara topic) the yeshiva is studying at the time. </p>
        <p>Typically, yeshiva students attend a daily shiur yomi (daily lecture) given by a maggid shiur (literally, "sayer of the shiur") and a weekly shiur klali (comprehensive lecture, which sums up the week's learning) given by the rosh yeshiva.[2] The rosh yeshiva usually also gives the top shiur on a daily basis. Before the shiur, a bibliography and a series of textual references are posted so that students may prepare for the lesson in advance. Students typically spend several hours preparing for the shiur yomi. After the shiur, students spend additional time reviewing and clarifying the lesson that they have just heard. These preparation and review periods take place in a special time period called a seder, in which students study the lesson individually and/or in chavrutot (study pairs). </p>
        <p>Shiurim may also be offered in yeshiva on topics in mussar, Chumash, and hashkafah (Jewish philosophy), depending on the yeshiva and the learning level of its students.[3]</p>
        <h2>Present Day Shiurim</h2>
        <p>In modern parlance, the term "shiur" has been extended to include any kind of Torah lesson — including lectures to children, women, working individuals, and taped lectures circulated via cassette tape, computer hookup, MP3 or MP4 connection, or call-in telephone lines. Though by definition, a shiur is a comprehensive, structured lesson.</p>
